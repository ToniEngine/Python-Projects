# Type casting = converts the data type of a value to another data type


x =1   # int
y = 2.0 #Float
z = "3" #String


print(str(x)) # We have type casted the int data type to a string data type using the str()
print(int(y)) # We have type casted the Float data type to an Integer Data type using the int()
print(float(z)) # We have type casted the string data type to a float Data type using the float()


print(y+float(z))
