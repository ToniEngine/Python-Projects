# Arithmetic Operation supported in python

print(10+3)  # Addition Operator
print(10-3)  # Subtraction Operator
print(10*3)  # Multiplication Operator
print(10/3)  # Division Operator
print(10//3) # Integer Division Operator
print(10%3)  # Modulus Operator
print(10**3) # Exponent Operator


# Augmented Assignment Operator
x = 10
# x = x + 3
print(x)
# Using the augmented assignment operator to increment a number
x += 3
print(x)

# Using the augmented assignment operator to decrement a number
y= 10
y -=3
print(y)





