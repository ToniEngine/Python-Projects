import math # We Imported the math module in python

print(math.pi) # It Prints out the value of pi

num = 3.14
print(round(num)) # The round() function is an in-built python function that rounds to the nearest whole integer
print(math.ceil(num)) # The ceil() function of the math class rounds the number up to the nearest whole integer
print(math.floor(num)) # The floor() method of the math clas rounds a number down to the nearest whole integer
num2 = -3.14
print(abs(num2))   # Gives the absolute value of a number("How far a no. is away from zero")

# The pow() function rasie a base number to a power
print(pow(num, 2)) # You pass it two arguments, a base and a power

# The sqrt() function gives the square root of a number
print(math.sqrt(num))
print(math.sqrt(100))
print(math.pow(8,2))
print(math.sqrt(64))


